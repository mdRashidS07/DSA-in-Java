public class SwitchDemo {
    public static void main(String[] args) {
        int rollno=18;
        
           switch (rollno)
           {
            case 15:System.out.println("Amit");
                      break;
             case 16:System.out.println("Farhan"); 
                     break;
            case 17: System.out.println("ismail"); 
                      break;
            case 18:System.out.println("Rashid"); 
                     break;
            default:System.out.println("invalid roll no"); 
                     break;                         

           }
    }
}
